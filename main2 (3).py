@app.post("/users/register", tags=["Users"])
def register_user(user: UserRegister, conn=Depends(get_db)):
    """
    Register a new user in the system

    - **email**: User's email address (unique)
    - **name**: Full name
    - **address**: Physical address
    - **birthdate**: Date of birth (YYYY-MM-DD)
    - **gender**: Male/Female/Other
    - **country**: Country of residence
    """
    # Check if user already exists
    exists = execute_query(
        conn,
        "SELECT email FROM users WHERE email = %s",
        (user.email,)
    )

    if exists:
        raise HTTPException(status_code=400, detail="User already exists")

    # Insert new user
    execute_query(
        conn,
        """
        INSERT INTO users (email, name, address, birthdate, gender, country)
        VALUES (%s, %s, %s, %s, %s, %s)
        """,
        (user.email, user.name, user.address, user.birthdate, user.gender, user.country),
        fetch=False
    )

    return {"message": "User created successfully", "email": user.email}

