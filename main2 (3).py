@app.get("/stats/tags-by-project", tags=["Statistics"])
def get_tags_by_project_type(conn=Depends(get_db)):
    """
    Get top 10 tags associated with each project type
    """
    # Get distinct project types
    project_types = execute_query(
        conn,
        "SELECT DISTINCT project_category FROM user_records WHERE project_category IS NOT NULL"
    )

    result = []

    for pt in project_types:
        project_type = pt['project_category']
        tags = execute_query(
            conn,
            """
            SELECT t.tag_name, COUNT(*) as usage_count
            FROM tags t
            JOIN user_records ur ON t.identifier = ur.identifier
            WHERE ur.project_category = %s
            GROUP BY t.tag_name
            ORDER BY usage_count DESC
            LIMIT 10
            """,
            (project_type,)
        )

        result.append({
            "project_category": project_type,
            "tags": tags
        })

    return result

