def get_usage_by_project_type(conn=Depends(get_db)):
    """
    Get distribution of dataset usage by project type with percentages
    """
    usage_stats = execute_query(
        conn,
        """
        SELECT
            project_category,
            COUNT(*) as count,
            ROUND(COUNT(*) * 100.0 / (SELECT COUNT(*) FROM user_records), 1) as percentage
        FROM user_records
        WHERE project_category IS NOT NULL
        GROUP BY project_category
        ORDER BY count DESC
        """
    )

    return usage_stats

