@app.get("/stats/distribution", tags=["Statistics"])
def get_distribution_stats(conn=Depends(get_db)):
    """
    Get comprehensive statistics about dataset distribution by:
    - Organization
    - Topic/Category
    - Format
    - Organization Type
    """
    stats = {}

    # By organization
    stats["by_organization"] = execute_query(
        conn,
        """
        SELECT o.org_name, o.org_type, COUNT(d.identifier) as count
        FROM organizations o
        LEFT JOIN datasets d ON o.org_name = d.org_name
        GROUP BY o.org_name, o.org_type
        ORDER BY count DESC
        LIMIT 10
        """
    )

    # By topic/category
    stats["by_topic"] = execute_query(
        conn,
        """
        SELECT category, COUNT(identifier) as count
        FROM datasets
        WHERE category IS NOT NULL AND category != ''
        GROUP BY category
        ORDER BY count DESC
        LIMIT 10
        """
    )

    # By format
    stats["by_format"] = execute_query(
        conn,
        """
        SELECT source_format, COUNT(DISTINCT identifier) as count
        FROM resources
        WHERE source_format IS NOT NULL AND source_format != ''
        GROUP BY source_format
        ORDER BY count DESC
        LIMIT 10
        """
    )

    # By organization type - Fixed: Use %% to escape literal % signs
    stats["by_org_type"] = execute_query(
        conn,
        """
        SELECT
            CASE
                WHEN LOWER(org_type) LIKE '%%federal%%' THEN 'Federal'
                WHEN LOWER(org_type) LIKE '%%state%%' THEN 'State'
                WHEN LOWER(org_type) LIKE '%%city%%' OR LOWER(org_type) LIKE '%%municipal%%' THEN 'City'
                ELSE 'Other'
            END as type_category,
            COUNT(DISTINCT d.identifier) as count
        FROM organizations o
        LEFT JOIN datasets d ON o.org_name = d.org_name
        GROUP BY type_category
        ORDER BY count DESC
        """
    )

    return stats

