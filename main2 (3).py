def execute_query(conn, sql: str, params: tuple = None, fetch: bool = True):
    """
    Execute SQL query with parameterized statements to prevent SQL injection

    Args:
        conn: Database connection
        sql: SQL query with %s placeholders
        params: Tuple of parameters (single value needs trailing comma)
        fetch: Whether to fetch results (False for INSERT/UPDATE/DELETE)

    Returns:
        Fetched rows or None
    """
    with conn.cursor() as cursor:
        # Using %s placeholder and params tuple prevents SQL injection
        cursor.execute(sql, params or ())

        if fetch:
            return cursor.fetchall()
        else:
            conn.commit()  # Must commit for INSERT/UPDATE/DELETE
            return None

