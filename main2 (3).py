    # Verify dataset exists
    dataset = execute_query(
        conn,
        "SELECT identifier FROM datasets WHERE identifier = %s",
        (usage.identifier,)
    )

    if not dataset:
        raise HTTPException(status_code=404, detail="Dataset not found")

    # Insert usage record
    execute_query(
        conn,
        """
        INSERT INTO user_records (email, identifier, project_name, project_category)
        VALUES (%s, %s, %s, %s)
        """,
        (usage.email, usage.identifier, usage.project_name, usage.project_category),
        fetch=False
    )

    return {"message": "Usage added successfully"}

